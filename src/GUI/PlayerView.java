    private void make_visibleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_make_visibleActionPerformed
        ArrayList<Treasure> selHidden = getSelectedTreasures (hiddenTreasures);
        napakalakiModel.makeTreasuresVisible (selHidden);
        setPlayer (napakalakiModel.getCurrentPlayer());
    }//GEN-LAST:event_make_visibleActionPerformed

    private void discard_treasuresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_discard_treasuresActionPerformed
        ArrayList<Treasure> selVisible = getSelectedTreasures(visibleTreasures);
        ArrayList<Treasure> selHidden = getSelectedTreasures(hiddenTreasures);
        napakalakiModel.discardHiddenTreasures(selHidden);
        napakalakiModel.discardVisibleTreasures(selVisible);
        
        setPlayer(napakalakiModel.getCurrentPlayer());
    }//GEN-LAST:event_discard_treasuresActionPerformed

