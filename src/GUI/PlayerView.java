    public void setPlayer(Player p){
        playerModel = p;
        this.name.setText(p.getName());
        this.level.setText("Level " + Integer.toString(p.getLevels()));
        
        if (p instanceof CultistPlayer)
            this.type.setText("Sectario");
        else
            if (p instanceof PlayerCorpulento)
                this.type.setText("Player Corpulento");
            else
                if (p instanceof PlayerCuadrupedo)
                    this.type.setText("Player Cuadrúpedo");
                else
                    this.type.setText("No sectario");
        
        this.fillTreasurePanel(visibleTreasures, p.getVisibleTreasures());
        this.fillTreasurePanel(hiddenTreasures, p.getHiddenTreasures());
        
        if (!playerModel.validState()){
            pendingBadConsequenceView2.setPendingBadConsequence(playerModel.getPendingBadConsequence());
            pendingBadConsequenceView2.setVisible(true);
        }
        
        repaint();
        revalidate();
    }
    
    private ArrayList<Treasure> getSelectedTreasures(JPanel aPanel) {
        // Se recorren los tesoros que contiene el panel,
        //almacenando en un vector aquellos que están seleccionados.
        //Finalmente se devuelve dicho vector.
        TreasureView tv;
        ArrayList<Treasure> output = new ArrayList();
        for (Component c : aPanel.getComponents()) {
            tv = (TreasureView) c;
            if ( tv.isSelected() )
                output.add ( tv.getTreasure() );
        }
        return output;
    }


